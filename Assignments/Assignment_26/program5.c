#include <stdio.h>

void display(int ino)
{

  int i = 0;
  for( i = 2; i <= ino*2; i= i+2)
  {
     printf("%d\t",i);
  }
    
}

////////////////////////////////////////////////////////////////////////
//  ENTRY POINT FUNCTION FOR THE APPLICATION
////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    
    printf("Enter the frequency \n");
    scanf("%d",&iValue);

    display(iValue);
    return 0;
}

