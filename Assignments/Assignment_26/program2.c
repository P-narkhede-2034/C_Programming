////////////////////////////////////////////////////////////////////////
//    REUIRED HEADER FILE 
////////////////////////////////////////////////////////////////////////

#include <stdio.h>

void display(int ino)
{
   int i = 0;

   for (i = ino ; i >= 1; i--)
   {
     if ((i % 2)== 0)
     {
        printf("%d\t",i/2);
     }
     else
     {
        printf("*\t");
     }
   }
}

////////////////////////////////////////////////////////////////////////
//  ENTRY POINT FUNCTION FOR THE APPLICATION
////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    
    printf("Enter the frequency \n");
    scanf("%d",&iValue);

    display(iValue);
    return 0;
}

