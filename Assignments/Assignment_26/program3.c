#include <stdio.h>

void display(int ino)
{
   int i = 0;
   
  
    for(i = 1 ; i <= ino ; i++ )
       { 
        if(i % 2 == 0)
        {
               printf("*\t");
        }
        else
        {
            printf("%d\t", (i/2)+1);

        }
       }
    
}

////////////////////////////////////////////////////////////////////////
//  ENTRY POINT FUNCTION FOR THE APPLICATION
////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    
    printf("Enter the frequency \n");
    scanf("%d",&iValue);

    display(iValue);
    return 0;
}

