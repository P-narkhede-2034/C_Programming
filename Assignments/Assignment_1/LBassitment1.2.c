#include <stdio.h>

////////////////////////////////////////////////////////////////////////
//      FUNCTION NAME  : void Display()
//      DESCRIPTION    : This function prints the word "Marvellous"
//                        five times on the screen.
//      INPUT          : Nothing
//      OUTPUT         : Nothing (void)
//      AUTHOR         : PRANAV MANOJ NARKHEDE
//      DATE           : 19/11/2025
