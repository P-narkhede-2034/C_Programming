void Display()
{
    int i = 0;
    for ( i = 1 ; i <= 5 ; i++)
    {
        printf("Marvellous\n");
    }
}

////////////////////////////////////////////////////////////////////////
//  ENTRY POINT FUNCTION FOR THE APPLICATION
////////////////////////////////////////////////////////////////////////
int main()
{
    Display();
    return 0;
}
