#include<stdio.h>

////////////////////////////////////////////////////////////////////////
//      FUNCTION NAME  : void Display()
//      DESCRIPTION    : This function prints numbers from 1 to 5 using
//                        a while loop.
//      INPUT          : Nothing
//      OUTPUT         : Nothing (void)
//      AUTHOR         : PRANAV MANOJ NARKHEDE
//      DATE           : 19/11/2025
////////////////////////////////////////////////////////////////////////
void Display()
{
   int i = 0;
   
   i = 1;
   while( i <= 5)
   {
      printf("%d",i);
      i++;
   }
}

////////////////////////////////////////////////////////////////////////
//  ENTRY POINT FUNCTION FOR THE APPLICATION
////////////////////////////////////////////////////////////////////////
int main()
{
    Display();
    return 0;
}
