////////////////////////////////////////////////////////////////////////
//  QUESTION : Check if number is divisible by 5
////////////////////////////////////////////////////////////////////////

////////////////////////////////////////////////////////////////////////
//    REQUIRED HEADER FILE 
////////////////////////////////////////////////////////////////////////
#include<stdio.h>

////////////////////////////////////////////////////////////////////////
//      FUNCTION NAME  : void CheckDivisible(int iNo)
//      DESCRIPTION    : This function checks if number is divisible by 5.
//      INPUT          : iNo
//      OUTPUT         : void
//      AUTHOR         : PRANAV MANOJ NARKHEDE
////////////////////////////////////////////////////////////////////////
void CheckDivisible(int iNo)
{
   if (iNo % 5 == 0)
   {
         printf("that no is divisible by 5");
   }
   else
   {
        printf("that no is not divisible by 5");
   }
}

////////////////////////////////////////////////////////////////////////
//  ENTRY POINT FUNCTION FOR THE APPLICATION
////////////////////////////////////////////////////////////////////////
int main()
{
    int iValue = 0;

    printf("Enter number : \n");
    scanf("%d",&iValue);

    CheckDivisible(iValue);

    return 0;
}
