////////////////////////////////////////////////////////////////////////
//  QUESTION : Print all numbers from 1 to N
////////////////////////////////////////////////////////////////////////

////////////////////////////////////////////////////////////////////////
//    REQUIRED HEADER FILE 
////////////////////////////////////////////////////////////////////////
#include<stdio.h>

////////////////////////////////////////////////////////////////////////
//      FUNCTION NAME  : void DisplayNumbers(int iNo)
//      DESCRIPTION    : This function prints numbers from 1 to N.
//      INPUT          : iNo
//      OUTPUT         : void
//      AUTHOR         : PRANAV MANOJ NARKHEDE
////////////////////////////////////////////////////////////////////////
void DisplayNumbers(int iNo)
{
    int i = 0;
    for (i = 0 ; i <= iNo ; i++)
    {
        printf("%d\t",i);
    }
}

////////////////////////////////////////////////////////////////////////
//  ENTRY POINT FUNCTION FOR THE APPLICATION
////////////////////////////////////////////////////////////////////////
int main()
{
    int iValue = 0;

    printf("Enter number : \n");
    scanf("%d",&iValue);

    DisplayNumbers(iValue);

    return 0;
}
